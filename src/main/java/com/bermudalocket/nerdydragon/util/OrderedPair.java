package com.bermudalocket.nerdydragon.util;

// ------------------------------------------------------------------------
/**
 * A class representing a simple ordered pair.
 */
public class OrderedPair {

    private final int _a, _b;

    public OrderedPair(int a, int b) {
        _a = a;
        _b = b;
    }

    public int getA() { return _a; }
    public int getB() { return _b; }

}
